package main

import "fmt"

func main() {
	x := 10
	y := 20

	fmt.Println("Valor de x:", x,". Valor de y:", y)
	fmt.Println("x == y ", x == y)
	fmt.Println("x != y ", x != y)
	fmt.Println(" x < y ", x < y)
	fmt.Println(" x > y ", x > y)
	fmt.Println(" x <= y ", x <= y)
	fmt.Println(" x >= y ", x >= y)
}